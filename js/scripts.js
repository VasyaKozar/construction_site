const buttons = document.querySelectorAll('.slides div.expand');
const modal = document.getElementById('myModal');
const modalTitle = document.getElementById('modal-title');
const modalList = document.getElementById('modal-list');
// const modalImg = document.getElementById('modal-img');
const closeBtn = modal.querySelector('.close');

buttons.forEach(button => {
  button.addEventListener('click', function(e) {
    e.preventDefault();

    const parent = this.closest('.col-md-4');

    const title = parent.getAttribute('data-title') || '';
    const listData = parent.getAttribute('data-list') || '';
    const imgSrc = parent.getAttribute('data-img') || '';

    modalTitle.textContent = title;
    // modalImg.src = imgSrc;
    // modalImg.alt = title;

    modalList.innerHTML = '';

    const items = listData.split('|');
    items.forEach(item => {
      const li = document.createElement('li');
      li.textContent = item.trim();
      modalList.appendChild(li);
    });

    modal.style.display = 'block';
  });
});

closeBtn.addEventListener('click', () => {
  modal.style.display = 'none';
});

window.addEventListener('click', (event) => {
  if (event.target === modal) {
    modal.style.display = 'none';
  }
});